					<h2>Antelope Valley YouthBuild</h2>
					<h5>Palmdale, CA - $10,000</h5>
					<p class="intro">In 2012 and 2013, Bike &amp; Builders volunteered on a project in the desert of Palmdale that stuck with many of our riders, so much so that 2013’s ME2SB team fully funded their grant. AV YouthBuild is refurbishing a property that will create units for up to 19 young adults in need of homes. Residents will either be current students in the YouthBuild program or recent graduates, particularly single mothers with children under the age of five.</p> 
				  </header>
				  </div>
				  <section class="content">
				<p>AV YouthBuild supports about 170 young people in Palmdale each year by working with them to obtain their high school diploma and various professional credentials while concurrently learning valuable job and leadership skills. Students spend half of their time in the classroom and half on jobsites developing skills in construction, nursing, solar energy production, and fire-fighting. The supportive housing facility will be rent-free, provided tenants maintain positive success in school or their career. When finished, the building will make AV YouthBuild the only YB program in the country with an accompanying housing unit. The facility is part of a long-term “adopt-a-block” strategy, where AV YouthBuild will work on reinvigorating the entire neighborhood, block-by-block. Next year, Bike &amp; Build can look forward to working on a duplex home for local low-income families.</p> 

				<p>One of aspects that drew Bike &amp; Build riders to AV YouthBuild was the chance they got to interact directly with the YB students on the build site. AV YouthBuild Executive Director Rossie Johnson agreed. “The Bike &amp; Builders are the same age as our YouthBuild students, so it’s a great exchange between the groups. It’s a great way for our students to see other young people ‘paying it forward,’ and I know the B&Bers really enjoy working with our group.”</p>

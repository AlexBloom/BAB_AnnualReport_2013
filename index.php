				<h5>Colorado Springs, CO - $55,000</h5>
				<p class="intro">For the last five summers, Pikes Peak Habitat for Humanity has served as a pre-arranged grant beneficiary for their support of two of our routes, NC2SD and SC2SC. Since the formation of SC2SC in 2009, Pikes Peak has hosted a blitz build with our riders, where Bike &amp; Build riders build as much as possible of a single home. This year, B&B helped construct the Kleine home. </p>

		<h5>Message from our Board and Staff</h5>
		<h2><strong>Bike &amp; Build 2013:</strong> <em>A Thousand Communities</em></h2>

		<p class="intro">A rider from Spokane, Washington. A host in Natchitoches, Louisiana. A build partner in “The Little Apple,” Manhattan, Kansas. A grantee in “The Big Apple,” New York City. Bike &amp; Build’s reach in 2013 was vast and deep, impacting people across the country in rural and urban settings, across cultures and demographics. Our ten routes move from town to town across the United States with one purpose: engaging young adults in service for the affordable housing cause. </p>

		<p>Our trips, composed of 30 strangers, become their own communities. Through highs and lows, headwinds and rain, the bonds that develop are powerful and awakening and create an additional level of meaning for our young adults. But beyond that internal microcosm, each team interacts with communities and their members on a level that creates an awareness and appreciation - on both sides of that interaction - of a community different from your own. A Thousand Communities mutually reached, impacted, and empowered.</p>

		<p>Over 250 riders, ages 18-25 and teeming with idealism and energy, met on the East Coast for Bike &amp; Build’s 2013 routes. Pedaling thousands of miles, these young adults touched almost 500 churches, community centers, and schools who graciously hosted our teams. Their generosity not only sustained us calorically and socially, but allowed us to financially support affordable housing organizations in roughly 250 cities and towns through our grant program. A Thousand Communities, connected and supporting each other.</p>

		<p>Collecting and redirecting donations to our affordable housing partners is an incredibly important, measurable, and cogent part of our program, but our reach extends into tangible and intangible realms that go beyond standard metrics. In this report, we feature 2013 riders who will be service advocates far longer than their B&amp;B summer; alumni who continue to make a difference; grant partners who are making the most of the funds they receive from Bike &amp; Build; community hosts who make our rides possible; and the sponsors that support our programs. </p>

		<p>We are grateful to each and every community that supports Bike &amp; Build and are proud of the impact that we had in 2013. We invite you learn more about how to Bike &amp; Build and how your town can be a part of Bike &amp; Build’s Thousand Communities.</p> 


					<p class="intro">Habitat for Humanity Portland/Metro East, engages middle and high school-aged students in their Youth United program. In 2013, they received Bike &amp; Build funds from the Central US route to support the building of a home compliant to Earth Advantage Platinum standards. This home will be the third sponsored by the Youth United program over the last five years, where students have taken an active role in the planning, fundraising, and construction of the home. By raising funds to achieve the Earth Advantage designation, Habitat for Humanity Portland/Metro East is increasing the value of the home now and in the future for its future owners: heating bills can be exorbitant for low-income residents, and future environmental features will enhance the home’s value in the decades to come. The students working on the home are gaining valuable knowledge a range of technical subjects as well.</p>

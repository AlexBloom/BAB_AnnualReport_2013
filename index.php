				<p>Since 2009, Bike &amp; Build has granted over $278,000 to Pikes Peak Habitat, through annual donations that have allowed Bike &amp; Build to put a significant stamp on five homes in the Colorado Springs area.  “It’s amazing to see how much work can be done in a short week’s time,” Andy Petersen, Pikes Peak Habitat’s Development Director, said of Bike &amp; Build riders.  “They are all over the place doing all sorts of different things.” The weeklong stay for the SC route becomes quite the community undertaking as well, so Pikes Peak Habitat helps B&B trip leaders coordinate their stay with a local church and brings in three meals per day. It’s that effort that will allow the Kleine’s to enjoy the dinner table that they haven’t had for lack of space in their current home. “The biggest thing I’m looking forward to is seeing our kids make their rooms their own. They’re really excited about being able to have their friends over.”</p>

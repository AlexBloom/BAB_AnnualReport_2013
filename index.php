		<h2>Bike &amp; Build Scholarship</h2>
		<p class="intro">Many young adults in the U.S. have the motivation to serve their communities but do not have the means. These are individuals who have lived the affordable housing crisis. In 2013, Bike &amp; Build set out to engage a young person in our rides who without financial support would not have been able to participate. Thanks to the generosity of donors, we exceeded our goal and awarded two Bike &amp; Build Scholarships!</p>

		<p>The Bike &amp; Build Scholarship increases access to our trips for young adults from at-risk backgrounds in our hometown of Philadelphia. It supports those who might not otherwise have this life-changing opportunity. Our two 2013 recipients, Da’Quan Tucker (full scholarship) and Daniel Farmer (half scholarship), were selected because of their compelling stories and interest in supporting affordable housing.</p> 
		
		<ul class="clearfix" id="recipients">
			<li>
				<img src="img/content/06scholarship/daquan.jpg" alt="Da’Quan on The Capital Ride"/>

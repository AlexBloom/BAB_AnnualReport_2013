			</li>
			<li>
				<img src="img/content/06scholarship/daniel.jpg" alt="Daniel on The Capital Ride"/>
				<strong> Daniel Farmer </strong> <br>
				(half scholarship)
			</li>
			
		</ul>

		<p>Da’Quan, Daniel, and their teammates pedaled 556 miles from Richmond, VA to Philadelphia as part of The Capital Ride in July. They cumulatively volunteered 312 build hours with 4 affordable housing orgs, advocated for the cause in 9 host communities, and raised $22,455.11 to support Bike &amp; Build and Rebuilding Together Philadelphia.</p>

		<blockquote>Bike &amp; Build gave me a personal perspective of how the affordable housing cause affects communities outside of my own,” Da’Quan said afterwards. “It also made me realize that I can do something about it. It’s inspiring to serve others and be a part of the solution.” <small>  <span class="quoteauth">Da'Quan Tucker</span> <span class="authnote">Da’Quan recently enlisted in the Navy, but hopes to lead a Bike &amp; Build trip one day.</span> </small> </blockquote>
		

